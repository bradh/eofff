    @Test
    public void checkParse() throws IOException {
        File original = new File("moov_original.bin");
        Files.write(
                original.toPath(),
                MOOV_BYTES,
                StandardOpenOption.CREATE,
                StandardOpenOption.TRUNCATE_EXISTING);
        ByteArrayParser parser = new ByteArrayParser();
        List<Box> boxes = parser.parse(MOOV_BYTES);
        assertEquals(boxes.size(), 1);
        Box box = boxes.get(0);
        assertTrue(box instanceof MovieBox);
        MovieBox moov = (MovieBox) box;
        assertTrue(moov.getFourCC().toString().equals("moov"));
        List<Box> nestedBoxes = moov.getNestedBoxes();
        assertEquals(nestedBoxes.size(), 3);
        checkMovieHeaderBox(nestedBoxes.get(0));
        checkTrackBox(nestedBoxes.get(1));
        checkUserDataBox(nestedBoxes.get(2));
    }

